package elifoot;

public class Player {

    String name;
    String position;
    int attack;
    int defense;
    int stamina;

    public Player(String name, String position, int attack, int defense, int stamina) {
        this.name = name;
        this.position = position;
        this.attack = attack;
        this.defense = defense;
        this.stamina = stamina;
    }
}
